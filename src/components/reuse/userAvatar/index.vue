    &.has-canvas {
      position: relative;
      top: 50%;
      transform: translateY(-50%);
    }
    &.shadow {
      box-shadow: 2px 4px 8px 0 rgba(12, 0, 42, .5);
    }
  }
  .user-sign {
    cursor: pointer;
  }
